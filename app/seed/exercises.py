def seed(db: Session) -> int:
    """幂等 seed：返回新插入条数。用 RETURNING 精确统计（rowcount 对 ON CONFLICT 可能报 -1）。"""
    inserted = 0
    for name, category, group, level, equipment, description in EXERCISES:
        new_id = db.execute(
            pg_insert(Exercise)
            .values(
                name=name,
                category=category,
                progression_group=group,
                level=level,
                equipment=equipment,
                description=description,
            )
            .on_conflict_do_nothing(index_elements=["name"])
            .returning(Exercise.id)
        ).scalar_one_or_none()
        if new_id is not None:
            inserted += 1
    return inserted
